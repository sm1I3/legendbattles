		return 'выход';
	}
	if (is_word($query,'почему')) {
		return 'почему';
	}
	if ($question) {
		return 'вопрос';
	}
	if (is_word($query,'дурак')) {
		return 'дурак';
	}
	if (is_word($query,'блядь') or is_word($query,'блять') or
	is_word($query,'гандон') or is_word($query,'ебану') or
	is_word($query,'ебать') or is_word($query,'ебись') or
