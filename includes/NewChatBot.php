function is_words($query, $words){
	$words = explode(";",$words);
	for ($i=1; $i<count($words); $i++){
		$n = is_word($query, $words[$i]);
		if (! $n){
			return 0;
		}
		$query = mb_substr ($query, $n);
	}
	return 1;
}

function is_word($query, $word){
	return ((preg_match("/".$word."/",$query))?1:0);
}

function GetMsg($query){
	
	$query = mb_strtolower($query);
	
	if ($query == $_SESSION['bot_last_msg'] and !is_word($query,'клиент')) {
		return 'повтор';
	}
	
	$question = strstr ($query, '?');
	
	if (is_words($query,'как;меня;зовут')){
		return'имя';
	}
	if (is_word($query,'ты') and is_word($query,'кто')) {
		return 'тыкто';
	}
	if (strstr ($query, ':)') or strstr ($query, ':-)')) {
		return 'улыбка';
	}
	if (strstr ($query, ':(') or strstr ($query, ':-(')) {
		return 'грустно';
	}
	/* Пополняем базу отсюда! */
	if (is_words($query,'на;ком;качаться') or is_words($query,'где;мне;качаться') or is_words($query,'где;прокачаться')) {
		return 'кач';
	} 
	if (is_word($query,'карта') or is_word($query,'карты') or is_word($query,'карту')) {
		return 'карта';
	}
	if (is_word($query,'клиент') or is_word($query,'client') or is_word($query,'автобой')) {
		return 'клиент';
	}
	if (is_word($query,'анекдот') or is_word($query,'анегдот')) {
		return 'анекдот';
	}
	if (is_words($query,'дай;денег') or is_words($query,'дай;rb') or is_words($query,'дай;артов') or is_words($query,'дай;вещи') or is_words($query,'дай;hr') or is_words($query,'дай;dhr') or is_words($query,'дай;dhr')) {
		return 'дайденег';
	}	
	/* дальше не пополняем */
	if (is_word($query, 'да')) {
		if ($question){
			return 'да?';
		}
		return 'да';
	}
	if (is_word($query,'нет')) {
		return 'нет';
	}
	if (is_word($query,'привет')) {
		return 'привет';
	}
	if (is_word($query,'здорова') or is_word($query,'здорово')) {
		return 'здорово';
	}
	if ($query == 'давай' or $query == 'давай.' or $query == 'давай!') {
